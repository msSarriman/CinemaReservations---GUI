    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String sql="select * from Customer where customer_email=? and customer_phone=?";
       
        try{
            
            pst=conn.prepareStatement(sql);
            pst.setString(1,user_field.getText());
            pst.setString(2,pass_field.getText());
            rs=pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog(null,"Your credentials are correct."+System.lineSeparator()+"Welcome to Cinema Database!"+System.lineSeparator()+"Enjoy your visit Customer!");
                close();
                
                
                customermaintab s=new customermaintab();
                s.setVisible(true);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Invalid combination of credentials. Please try again.");
            }
        }
            catch(Exception e){
                  JOptionPane.showMessageDialog(null, e); 
            }  
    }//GEN-LAST:event_jButton1MouseClicked

